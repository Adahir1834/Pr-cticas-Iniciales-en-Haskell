doble x = x * 2

--Funcionalidad:
--	La función doble toma un número entero como entrada y devuelve el doble de ese número.
-- Es decir, multiplica el número por 2 para obtener su valor duplicado.
--Parámetros que recibe:
---	x: Un número entero al cual se le desea calcular el doble.
--Resultado esperado:
--	El resultado será un número entero que es el doble del número proporcionado como argumento.
