mitad x = x / 2

--Funcionalidad: 
--	La función mitad toma un número de punto flotante como entrada y devuelve 
--la mitad de ese número. Es decir, divide el número entre 2 para obtener la mitad del valor original.
--Parámetros que recibe:
--	x: Un número de punto flotante al cual se le desea calcular la mitad.
--Resultado esperado:
--	El resultado será un número de punto flotante que es la mitad del número proporcionado como argumento.
